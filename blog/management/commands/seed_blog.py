class Command(BaseCommand):
    help = "Seed the blog with 10 realistic Persian woodworking posts, tags, and downloaded images."

    def add_arguments(self, parser):
        parser.add_argument(
            "--clear",
            action="store_true",
            help="Delete all existing blog posts before seeding.",
        )

    # ── helpers ──────────────────────────────────────────────────────────────

    def _get_or_create_tags(self):
        """Ensure all tags exist and return a dict {name: Tag}."""
        tag_map = {}
        for name in TAGS_DATA:
            tag, created = Tag.objects.get_or_create(
                name=name,
                defaults={"slug": slugify(name, allow_unicode=True)},
            )
            tag_map[name] = tag
            if created:
                self.stdout.write(f"  [tag+] {name}")
        return tag_map

    def _download_image(self, seed, index):
        """
        Download a placeholder image from picsum.photos using a fixed seed
        so re-runs always get the same image. Returns a ContentFile or None.
        """
        url = f"https://picsum.photos/seed/{seed}/800/600"
        try:
            self.stdout.write(f"  [img ] Fetching {url} …", ending="")
            resp = requests.get(url, timeout=15)
            resp.raise_for_status()
            filename = f"blog_seed_{index:02d}_{seed}.jpg"
            self.stdout.write(" OK")
            return ContentFile(resp.content, name=filename)
        except requests.RequestException as exc:
            self.stdout.write(self.style.WARNING(f" FAILED ({exc})"))
            return None

    # ── main handle ──────────────────────────────────────────────────────────

    def handle(self, *args, **options):
        # ── 1. Optionally clear existing posts ───────────────────────────────
        if options["clear"]:
            count, _ = Post.objects.all().delete()
            self.stdout.write(self.style.WARNING(f"Deleted {count} existing post(s)."))

        # ── 2. Resolve author ─────────────────────────────────────────────────
        author = User.objects.filter(is_superuser=True).first() or User.objects.first()
        if author is None:
            raise CommandError(
                "No users found in the database. "
                "Create a superuser first with: python manage.py createsuperuser"
            )
        self.stdout.write(f"Author: {author} (pk={author.pk})")

        # ── 3. Create / fetch tags ─────────────────────────────────────────────
        self.stdout.write("\nEnsuring tags …")
        tag_map = self._get_or_create_tags()

        # ── 4. Create posts ────────────────────────────────────────────────────
        self.stdout.write("\nCreating posts …\n")
        created_count = 0
        skipped_count = 0

        for index, data in enumerate(POSTS_DATA):
            title = data["title"]

            if Post.objects.filter(title=title).exists():
                self.stdout.write(
                    self.style.WARNING(f"  [skip] Already exists: {title}")
                )
                skipped_count += 1
                continue

            self.stdout.write(f"[{index + 1:02d}/10] {title}")

            # Build slug
            slug = slugify(title, allow_unicode=True)
            # Guarantee uniqueness
            if Post.objects.filter(slug=slug).exists():
                slug = f"{slug}-{index + 1}"

            # Create the post (no image yet)
            post = Post(
                title=title,
                slug=slug,
                author=author,
                content=data["content"],
                status="published",
            )

            # Download and attach image
            image_seed = IMAGE_SEEDS[index % len(IMAGE_SEEDS)]
            image_file = self._download_image(image_seed, index + 1)
            if image_file:
                post.image.save(image_file.name, image_file, save=False)
            else:
                self.stdout.write(
                    self.style.WARNING("  [warn] Post saved without image.")
                )

            post.save()

            # Attach tags
            post_tags = [tag_map[name] for name in data["tags"] if name in tag_map]
            post.tags.set(post_tags)

            self.stdout.write(
                self.style.SUCCESS(
                    f"  [ok  ] Saved — pk={post.pk}, tags={len(post_tags)}"
                )
            )
            created_count += 1

            # Brief pause to be polite to picsum.photos
            time.sleep(0.4)

        # ── 5. Summary ────────────────────────────────────────────────────────
        self.stdout.write("\n" + "─" * 50)
        self.stdout.write(
            self.style.SUCCESS(
                f"Done. Created: {created_count}  |  Skipped (already existed): {skipped_count}"
            )
        )
